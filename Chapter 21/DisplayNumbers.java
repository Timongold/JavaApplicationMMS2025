public class DisplayNumbers{
	public static void main(String[] args){
		//Using println
		System.out.println("1, 2, 3, 4");
		
		//Using print
        System.out.print("1 ");
        System.out.print("2 ");
        System.out.print("3 ");
        System.out.print("4");
		
		//Using printf
		int num1 = 1;
		int num2 = 2;
		int num3 = 3;
		int num4 = 4;
        System.out.printf("%n%d %d %d %d%n", num1, num2, num3, num4);
    
	}
}